string is_sorted_and_how( vector<int> array )
{
    if ( is_sorted( array.begin(), array.end() ))
    {
        return "yes, ascending";
    }

    if ( is_sorted( array.begin(), array.end(), greater<int>() ))
    {
        return "yes, descending";
    }
    
    return "no";
}
